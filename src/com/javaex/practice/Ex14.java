package com.javaex.practice;

public class Ex14 {
	public static void main(String[] args) {
		int a=5;
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a);
		System.out.println(a--);
		System.out.println(a);
		
		//예상: 5 6 6 6 7 6 6 6 5
	}

}
